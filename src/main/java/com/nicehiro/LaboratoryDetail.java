package com.nicehiro;

public class LaboratoryDetail {

    private String name;
    private String apparatus;
    private int capacity;
    private String email;
    private String phone;

    public LaboratoryDetail(String name, String apparatus, int capacity, String email, String phone) {
        this.name = name;
        this.apparatus = apparatus;
        this.capacity = capacity;
        this.email = email;
        this.phone = phone;
    }
}
